
def check_words(strr):
    FORBIDDEN_WORDS = ['запрещенные слова']
    ALLOWED_WORDS = {'1', '2', '3', '4', '5', '6', '7', '8', '9', '0', 'а', 'б', 'в', 'г', 'д', 'е', 'ё', 'ж', 'з', 'и', 'й', 'к', 'л', 'м', 'н', 'о', 'п', 'р', 'с', 'т', 'у', 'ф', 'х', 'ц', 'ч', 'ш', 'щ', 'ъ', 'ы', 'ь', 'э', 'ю', 'я', ',', '.', '!', '?', ' ', '\n', '-', '«', '»', '\"'}

    return (set(strr.lower()) & ALLOWED_WORDS) == set(strr)
    
